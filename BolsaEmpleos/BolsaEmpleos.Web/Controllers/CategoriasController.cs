using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Threading.Tasks;
using System.Net;
using System.Web;
using System.Web.Mvc;
using BolsaEmpleos.Data.Model;
using BolsaEmpleos.Data.UnitOfWork;

namespace BolsaEmpleos.Web.Controllers
{
    public class CategoriasController : Controller
    {
        private UnitOfWork unitOfWork = new UnitOfWork();

        // GET: Categorias
        public ActionResult Index()
        {
            return View(unitOfWork.CategoriaRepository.Get());
        }

        // GET: Categorias/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Categorias/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Id_Categoria,Nombre,Estado")] Categoria categoria)
        {
            if (ModelState.IsValid)
            {
                unitOfWork.CategoriaRepository.Insert(categoria);
                unitOfWork.Save();
                return RedirectToAction("Index");
            }

            return View(categoria);
        }

        // GET: Categorias/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Categoria categoria = unitOfWork.CategoriaRepository.GetByID(id);
            if (categoria == null)
            {
                return HttpNotFound();
            }
            return View(categoria);
        }

        // POST: Categorias/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id_Categoria,Nombre,Estado")] Categoria categoria)
        {
            if (ModelState.IsValid)
            {
                unitOfWork.CategoriaRepository.Update(categoria);
                unitOfWork.Save();
                return RedirectToAction("Index");
            }
            return View(categoria);
        }

        // GET: Categorias/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Categoria categoria = unitOfWork.CategoriaRepository.GetByID(id);
            if (categoria == null)
            {
                return HttpNotFound();
            }
            return View(categoria);
        }

        // POST: Categorias/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Categoria categoria = unitOfWork.CategoriaRepository.GetByID(id);
            unitOfWork.CategoriaRepository.Delete(categoria);
            unitOfWork.Save();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            unitOfWork.Dispose();
            base.Dispose(disposing);
        }
    }
}
